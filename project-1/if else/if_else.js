// find maximum between two number
function two() {
  let a = 10,
    b = 20;

  if (a > b) {
    console.log("a is max");
  } else {
    console.log("b is max");
  }
}
// find maximum between three number

function three() {
  let a = 10,
    b = 20,
    c = 30;

  if (a > b) {
    console.log("a is max");
  } else if (b > c) {
    console.log("b is max");
  } else {
    console.log("c is max");
  }
}
